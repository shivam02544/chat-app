import { Schema, model, models } from "mongoose";
const users = new Schema({
  fullname: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
  },
});
const Users = models.User || model("User", users);
export default Users;
